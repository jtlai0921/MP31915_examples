#include<iostream>

using namespace std;

int main()
{	
	int A=21,B=6;
	//算術運算子的各種運算與結果 
    cout<<"A=21,B=6"<<" A+B="<<A+B<<endl;
    cout<<"A=21,B=6"<<" A-B="<<A-B<<endl;
    cout<<"A=21,B=6"<<" A*B="<<A*B<<endl;
    cout<<"A=21,B=6"<<" A/B="<<A/B<<endl;
    cout<<"A=21,B=6"<<" A%B="<<A%B<<endl;//餘數運算子的使用 
    

    return 0;
} 
